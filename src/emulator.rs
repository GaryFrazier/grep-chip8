const FONTSET_SIZE: usize = 80;

const FONTSET: [u8; FONTSET_SIZE] = [
    0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
    0x20, 0x60, 0x20, 0x20, 0x70, // 1
    0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
    0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
    0x90, 0x90, 0xF0, 0x10, 0x10, // 4
    0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
    0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
    0xF0, 0x10, 0x20, 0x40, 0x40, // 7
    0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
    0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
    0xF0, 0x90, 0xF0, 0x90, 0x90, // A
    0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
    0xF0, 0x80, 0x80, 0x80, 0xF0, // C
    0xE0, 0x90, 0x90, 0x90, 0xE0, // D
    0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
    0xF0, 0x80, 0xF0, 0x80, 0x80  // F
];

#[derive(Debug)]
pub struct Emulator {
    pub pc: u16,
    pub sp: u8,
    pub stack: [u16; 0x10], 
    pub ram: [u8; 0x1000],
    pub display_memory: [bool; 0x800],
    pub v: [u8; 0x10],
    pub i: u16,

    //	When these registers are non-zero, they are automatically decremented at a rate of 60Hz. See the section 2.5, Timers & Sound, for more information on these.
    pub delay_timer: u8,
    pub sound_timer: u8,

    pub keys: [bool; 0x10],
}

impl Default for Emulator {
    fn default() -> Self {
        let mut emu = Emulator {
            pc: 0,
            sp: 0,
            stack: [0; 0x10],
            ram: [0; 0x1000],
            display_memory: [false; 0x800],
            v: [0; 0x10],
            i: 0,
            delay_timer:0,
            sound_timer: 0,
            keys: [false; 0x10]
        };

        emu.ram[..FONTSET_SIZE].copy_from_slice(&FONTSET);
        
        return emu;
    }
}